package Objetos;

import java.io.Serializable;

/**
 *
 * @author jcsr
 */
public class Mandar implements Serializable{
    String texto;
    String usuario;

    public Mandar(String texto, String usuario) {
        this.texto = texto;
        this.usuario = usuario;
    }

    public String getTexto() {
        return texto;
    }

    public void setTexto(String texto) {
        this.texto = texto;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }
    
    
}
